function varargout = createDipyramidalPentagonalPrism(xl1,xl2)

% xl1 is the height of the pyramid, and xl2 is the length of the prism body

x0 = 0; dx1 = xl1; dx2 = xl2;
y0 = 0; dy = 1;
z0 = 0; dz = 1;

nodes = [ x0 y0+(.5)*dy z0+(.5)*dz ; x0+dx1 y0+(.5)*dy z0+dz ; ...
    x0+dx1 y0+dy z0+(.75)*dz ; x0+dx1 y0+(.75)*dy z0 ; ...
    x0+dx1 y0+(.25)*dy z0 ; x0+dx1 y0 z0+(.75)*dz ;...
    x0+2*dx1+dx2 y0+(.5)*dy z0+(.5)*dz ; x0+dx1+dx2 y0+(.5)*dy z0+dz ; ...
    x0+dx1+dx2 y0+dy z0+(.75)*dz ; x0+dx1+dx2 y0+(.75)*dy z0 ;...
    x0+dx1+dx2 y0+(.25)*dy z0 ; x0+dx1+dx2 y0 z0+(.75)*dz ];

edges = [ 1 2 ; 1 3 ; 1 4 ; 1 5 ; 1 6 ; 2 3 ; 3 4 ; 4 5 ; 5 6 ; 6 2 ; 2 8 ;...
    3 9 ; 4 10 ; 5 11 ; 6 12 ; 8 9 ; 9 10 ; 10 11 ; 11 12 ; 12 8 ; 7 8 ; 7 9 ;...
    7 10 ; 7 11 ; 7 12 ];

faces = {[ 1 2 3] ,[ 1 3 4], [1 4 5], [ 1 5 6], [ 1 6 2], [ 2 8 9 3], ...
    [ 3 9 10 4], [ 4 10 11 5], [ 5 11 12 6], [ 6 12 8 2], [ 7 8 12], [ 7 9 8],...
    [ 7 10 9], [ 7 11 10], [ 7 12 11]};

% format output
varargout = formatMeshOutput(nargout, nodes, edges, faces);  